  "B1-C11": "马车在归途的雷雨里倾覆,你与妻子失散。浑身湿透,你摸回梅伯里的空屋,走到了对着公地的书房窗前。",
  "B1-C16": "镜头转向伦敦的弟弟。黑烟封住了泰晤士河的桥,整座首都在一夜之间倾巢出逃。",
